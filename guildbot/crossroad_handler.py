    def _normalize_phrase(self, phrase: str) -> str:
        normalized = phrase.lower()
        normalized = normalized.replace("...", " ").replace("…", " ")
        normalized = normalized.replace("—", "-")
        normalized = re.sub(r"[!?.,:;\"«»()]", "", normalized)
        normalized = re.sub(r"\s+", " ", normalized)
        return normalized.strip(" /-")

    def _build_reverse_mapping(self, event_to_phrases: Dict[str, List[str]]) -> Dict[str, List[str]]:
        phrase_to_events: Dict[str, List[str]] = {}
        for event, phrases in event_to_phrases.items():
            for phrase in phrases:
                normalized_phrase = self._normalize_phrase(phrase)
                if not normalized_phrase:
                    continue
                phrase_to_events.setdefault(normalized_phrase, [])
                if event not in phrase_to_events[normalized_phrase]:
                    phrase_to_events[normalized_phrase].append(event)
        return phrase_to_events

    def _phrase_matches_key(self, input_phrase: str, key_phrase: str) -> bool:
        if not input_phrase or not key_phrase:
            return False
        if " " not in key_phrase:
            return input_phrase == key_phrase
        wrapped_input = f" {input_phrase} "
        wrapped_key = f" {key_phrase} "
        return wrapped_key in wrapped_input

    async def handle(self, message: Message) -> Optional[str]:
        text = message.text or ""
        if message.fwd_messages:
            text = message.fwd_messages[0].text or ""

        prefix = "Вы выходите к широкому перекрестку подземных путей."
        s = text.strip()
        if not s.startswith(prefix):
            return None
        west: Optional[Tuple[str, str]] = None
        north: Optional[Tuple[str, str]] = None
        east: Optional[Tuple[str, str]] = None
        for line in s.splitlines():
            t = line.strip()
            if t.startswith("🚩Западный путь:"):
                m = re.findall(r'"([^"]+)"', t)
                if len(m) >= 2:
                    west = (m[0], m[1])
            elif t.startswith("🚩Северный путь:"):
                m = re.findall(r'"([^"]+)"', t)
                if len(m) >= 2:
                    north = (m[0], m[1])
            elif t.startswith("🚩Восточный путь:"):
                m = re.findall(r'"([^"]+)"', t)
                if len(m) >= 2:
                    east = (m[0], m[1])

        if not (west and north and east):
            logger.warning(f"Insufficient crossroad data in: {s}")
            return "Не удалось распознать перекресток"
        def cats(phrase: str) -> set[str]:
            p = self._normalize_phrase(phrase)
            res: set[str] = set[str]()
            for k, v in self.dict.items():
                if self._phrase_matches_key(p, k):
                    res.update(v)
            return res
        west_set = cats(west[0]) & cats(west[1])
        north_set = cats(north[0]) & cats(north[1])
        east_set = cats(east[0]) & cats(east[1])
        west_answer = ", ".join(sorted(west_set)) if west_set else "ничего"
        north_answer = ", ".join(sorted(north_set)) if north_set else "ничего"
        east_answer = ", ".join(sorted(east_set)) if east_set else "ничего"
        return f"Запад: {west_answer}\nСевер: {north_answer}\nВосток: {east_answer}"